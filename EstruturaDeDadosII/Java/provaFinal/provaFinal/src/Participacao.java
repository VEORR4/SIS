public class Participacao {
     private int id;
     private int id_funcionario;
     private int id_projeto;
     private int horas_trabalhadas;

     public void setId(int id){
          this.id = id;
     }
     public int getId(int id){
         return this.id;
     }


     public void setId_funcionario(int id_funcionario){
          this.id_funcionario = id_funcionario;
     }
     public int getId_funcionario(int id_funcionario){
          return this.id_funcionario;
     }


     public void setId_projeto(int id_projeto){
          this.id_projeto = id_projeto;
     }
     public int getId_projeto(int id_projeto){
          return this.id_projeto;
     }

     
     public void setHoras_trabalhadas(int horas_trabalhadas){
          this.horas_trabalhadas = horas_trabalhadas;
     }
     public int getHoras_trabalhadas(int horas_trabalhadas){
          return this.horas_trabalhadas;
     }
}     






